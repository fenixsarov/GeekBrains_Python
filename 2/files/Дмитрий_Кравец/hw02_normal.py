from math import sqrt

lst1 = [2, -5, 8, 9, -25, 25, 4]
new_list = []

for i in range(len(lst1)):
    if lst1[i] > 0:
        if sqrt(lst1[i]).is_integer():
            new_list.append(int(sqrt(lst1[i])))

print('Новый список: {}'.format(new_list))


