lst = [1,2,4,5,6,2,5,2]
lst2 = []
for i in range(len(lst)):
    count = 0
    for j in range(len(lst)):
        if lst[i] == lst[j]:
            count += 1
    if count < 2:
        lst2.append(lst[i])

print('Новый список: {}'.format(lst2))