# Задача-3:
# Дан произвольный список из целых чисел. Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

# 2016.12.04 00:53:50 checked. P.Rusanov
# Отлично!
lst_int = [1, 2, 4, 5, 6, 8, 16, 23, 32, 11]
new_list = []

for i in range(len(lst_int)):       # Лучше итерироватся сразу по последовательности, а не по индексам
    if not lst_int[i] % 2:
        new_list.append(lst_int[i] / 4)
    else:
        new_list.append(lst_int[i] * 2)

print('Новый список: {}'.format(new_list))